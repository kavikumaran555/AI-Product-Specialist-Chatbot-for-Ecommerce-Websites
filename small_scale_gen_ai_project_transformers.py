#applying the text to numerical vector conversion model into the combined dataset.
product_embeddings = model.encode(combined.tolist())
print(product_embeddings)

#creating general list of user messages for chat to understand and reply customer humanly.

greetings = ["hi","hello","hey","are you there","anyone"]
