#creating a function for chatbot to take input and how to respond back.
def chatbot_response(message):
    user_msg = message.lower()

    #Convert user question to vector numbers called embedding.
    query_embedding = model.encode([user_msg])

    #finding vector similarities of question and answers using cosine.
    #words matching with question will have higher values.
    similarities = cosine_similarity(query_embedding, product_embeddings)

    #taking out the index number of the highest matching value.
    best_match = np.argmax(similarities)

    #now the index for question is row 0 since there is one row/one question.
    #index for highest matching value is stored in best_match.
    #we find the similarity value present in both row index, column index.
    score = similarities[0][best_match]

    # If question to product details match is good, then return product details
    if score >= 0.20:

        #using iloc to take out the best matching values from product and description.
        product = df.iloc[best_match]["product"]
        description = df.iloc[best_match]["description"]

        return f"Great! I recommend, {product.title()}: {description}"

    #response if user sends hi, hello messages.
    if any(word in user_msg for word in greetings):
        return "Hello! Hope you have a great day. Happy to answer your questions"

    #response if user sends how are you.
    if any(word in user_msg for word in how_are_you_phrases):
        return "I'm doing great! How can I help you today?"

    #response if user shares their preferences.
    if "like" or "love" or "mostly" or "hardly" or "sometimes" or "need" in user_msg:
        return "Great, thats great to hear!"

    #response if user send thanks etc.
    if any(word in user_msg for word in courtesy):
        return "Great! I am here if you have anymore questions"

    #response if user send bye etc.
    if any(word in user_msg for word in bye_words):
        return "Its a pleasure to assist you. Goodbye! Have a nice day."

    #response if user send any message other than what is predicted above.
    return "Sorry, I do not have that  information available yet."

#creating function for chat input appearance.
def send_message(b):

    #global asks the function to use chat_history variable which is already present outside the function.
    global chat_history

    #values entered in input_box will be taken as user text.
    user_text = input_box.value

    #if user did not type anything and click send, stop the function.
    if user_text == "":
        return

    #now user text is sent to Chatbot_response function, it will run and store the result in bot_reply
    bot_reply = chatbot_response(user_text)

    #user message will be added to the Chat_history.
    chat_history += f"<div style='text-align:right;margin:8px'><b>You:</b> {user_text}</div>"

    #bot_reply message will also be added to the chat_history.
    chat_history += f"<div style='text-align:left;margin:8px;color:darkblue'><b>Joseph AI:</b> {bot_reply}</div>"

    #Creating background for chat interface using HTML by iPywidget library.
    chat_area.value = f"<div style='height:420px;width:520px;border:1px solid gray;border-radius:10px;padding:10px;overflow:auto;background:#f9f9f9'>{chat_history}</div>"

    #value in input_box will be empty by default.
    input_box.value = ""


